//Que 2 WAP to swap two numbers without using third variable.

#include <stdio.h>

void main()
{
	int a,b;
	
	printf("\t\t\t:::::::::: Swap the Numbers :::::::::\n\n");
	
	printf("\n\t\tEnter Number A :");
	scanf("%d",&a);
	
	printf("\n\t\tEnter Number B :");
	scanf("%d",&b);
	
	printf("\n\t\tNumbers Before Swapping :");
	printf("\n\t\tA= %d",a);
	printf("\n\t\tB= %d",b);

}